// Faça um script que receba três valores constantes da idade de uma pessoa expressa em anos, 
// meses e dias, calcule e mostre no console a idade dessa pessoa expressa apenas em dias. 
// Considerar ano com 365 dias e mês com 30 dias.

var anos:number = 58;
var meses:number = 7;
var dias:number = 13;
var total:number = anos * 365 + meses * 30 + dias;
console.log("Idade de uma pessoa em dias:",total); 